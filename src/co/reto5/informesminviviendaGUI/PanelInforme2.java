    public void mostrarDatos() {
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }

        conexion objConexion = new conexion();

        try {

            ResultSet resultado = objConexion.consultarRegistros("SELECT ID_Proyecto, Constructora, Numero_Habitaciones, Ciudad\n"
                    + "FROM Proyecto\n"
                    + "WHERE Clasificacion=\"Casa Campestre\" AND \n"
                    + "(Ciudad=\"Santa Marta\" OR Ciudad=\"Cartagena\" OR Ciudad=\"Barranquilla\");");

            while (resultado.next()) {

                Object[] proyecto = {resultado.getInt("ID_Proyecto"),
                    resultado.getString("Constructora"),
                    resultado.getDouble("Numero_Habitaciones"),
                    resultado.getString("Ciudad")
                };

                model.addRow(proyecto);
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }

